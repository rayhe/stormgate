/** Cached name-to-index map for fast lookups. */
const nameCache = new Map<string, number>();

function buildNameCache(): void {
  if (nameCache.size > 0) return;
  for (let sn = 0; sn < skillTable.length; sn++) {
    const name = skillTable[sn].name.toLowerCase();
    if (name && name !== 'reserved') {
      nameCache.set(name, sn);
    }
  }
}

/**
 * Look up a skill/spell by name (prefix match, case insensitive).
 * Returns the skill number (index into skillTable) or -1 if not found.
 */
export function skillLookup(name: string): number {
  if (!name) return -1;
  const lower = name.toLowerCase();

  buildNameCache();

  // Exact match first
  const exact = nameCache.get(lower);
  if (exact !== undefined) return exact;

  // Prefix match
  for (let sn = 0; sn < skillTable.length; sn++) {
    const skillName = skillTable[sn].name;
    if (!skillName) continue;
    if (skillName.toLowerCase().startsWith(lower)) {
      return sn;
    }
  }

  return -1;
}

/**
 * Look up a skill/spell by its slot number.
 * Returns the skill number (index into skillTable) or 0 if not found.
 */
export function slotLookup(slot: number): number {
  for (let sn = 0; sn < skillTable.length; sn++) {
    if (skillTable[sn].slot === slot) {
      return sn;
    }
  }
  console.error(`slotLookup: no skill with slot ${slot}`);
  return 0;
}

/**
 * Check if a skill number is valid.
 */
export function isSn(sn: number): boolean {
  return sn >= 0 && sn < skillTable.length;
}

/**
 * Get the minimum class level for a character to use a skill.
 * Returns the lowest level across the character's class.
 */
export function skillLevel(sn: number, charClass: number): number {
  if (sn < 0 || sn >= skillTable.length) return L_APP;
  if (charClass < 0 || charClass >= skillTable[sn].levels.length) return L_APP;
  return skillTable[sn].levels[charClass];
}

/**
 * Register a spell function for a skill table entry (used by spells.ts to wire up implementations).
 */
export function registerSpellFn(sn: number, fn: SpellFn): void {
  if (sn >= 0 && sn < skillTable.length) {
    skillTable[sn].spellFn = fn;
  }
}

/**
 * Register a spell function by skill name.
 */
export function registerSpellByName(name: string, fn: SpellFn): void {
  const sn = skillLookup(name);
  if (sn >= 0) {
    skillTable[sn].spellFn = fn;
  }
}

