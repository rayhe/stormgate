export type SpellFn = (sn: number, level: number, ch: CharData, vo: unknown) => number;

// ============================================================================
//  SkillEntry interface
// ============================================================================

export interface SkillEntry {
  name: string;
  /** Per-class minimum level (21 classes); 999 = not available to that class. */
  levels: number[];
  /** Spell/skill implementation function, or null if passive / unimplemented. */
  spellFn: SpellFn | null;
  target: TargetType;
  minPosition: Position;
  minMana: number;
  /** Wait time in combat pulses after use. */
  beats: number;
  /** Noun shown in damage messages (e.g., "fireball"). */
  damageNoun: string;
  /** Message shown when the affect wears off. */
  offMessage: string;
  /** Unique slot number for object spell loading. */
  slot: number;
  /** true = spell (cast), false = skill (use). */
  isSpell: boolean;
  /** Message shown on failure. */
  failMessage: string;
}

